package ex_10_For_Loop;

public class Lab103_For_If_Loop {
    public static void main(String[] args) {
        for(int age=0; age < 18; age++)
        {
            if(age>15)
            {
                System.out.println("Receive a Gift");
            }
            else {
                System.out.println("No Gift");
            }

        }
    }
}
